package com.hbsi.domain;

import java.util.List;

public class BreakStockDomain {

	//表头文件
    private WireRopeForExport wireRopeForExport;
    //wireData数据列表
    private List<WireDataForExport> wireDataForExporlist;
//    Get和Set方法
	public WireRopeForExport getWireRopeForExport() {
		return wireRopeForExport;
	}
	public void setWireRopeForExport(WireRopeForExport wireRopeForExport) {
		this.wireRopeForExport = wireRopeForExport;
	}
	public List<WireDataForExport> getWireDataForExporlist() {
		return wireDataForExporlist;
	}
	public void setWireDataForExporlist(List<WireDataForExport> wireDataForExporlist) {
		this.wireDataForExporlist = wireDataForExporlist;
	}
	//无参构造
	public BreakStockDomain() {}
//	有参构造
	public BreakStockDomain(WireRopeForExport wireRopeForExport, List<WireDataForExport> wireDataForExporlist) {
		super();
		this.wireRopeForExport = wireRopeForExport;
		this.wireDataForExporlist = wireDataForExporlist;
	}
    
}
